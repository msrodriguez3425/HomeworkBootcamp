@app.route("/api/v1.0/<start>/<end>")
def calculator(start,end):

    collect_values = False
    temp_sum = 0
    temp_count = 0
    max_val = 0
    min_val = 1000
    for i in range(0,len(temp_dict["date"])):
        if temp_dict["date"] == start:
            collect_values = True
        if temp_dict["date"] == end:
            collect_values = False 

        #
        if collect_values == True:
            #summing for the average
            temp_sum = temp_sum + temp_dict["temperature"][i] 
            temp_count = temp_count + 1

            #Finding max and min temps
            current_val = temp_dict["temperature"][i]
            if current_val > max_val:
                max_val = current_val
            if current_val < min_val:
                min_val = current_val

    av_temp = temp_sum/temp_count

    results_list = [av_temp,max_val,min_val]

    return jsonify(results_list)
